    public function login(Request $request) {
      $email = $request->input("email");
      $password = $request->input("password");

      $user = Users::All()->where("email", $email)->where("password", $password);

      if(count($user) > 0) {
        $request->session()->put("user", json_encode(["user_id" => $user[0]['User_Id'], "user_role" => $user[0]['user_role']]));
        return redirect("users/view");
      } else {
        return view("userAuth.index");
      }
    }

